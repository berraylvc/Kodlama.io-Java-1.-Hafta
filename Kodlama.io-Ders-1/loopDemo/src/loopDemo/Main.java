package loopDemo;

public class Main {

	public static void main(String[] args) {
		// for
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		System.out.println("Birinci for dongusu bitti");
		
		for(int k=2;k<20;k+=2) {
			System.out.println(k);
		}
		System.out.println("Ikinci for dongusu bitti");
		
		
		//while
		 int i = 1;
		 while(i<=10) {
			 System.out.println(i);
			 i++;
		 }
		 System.out.println("while dongusu1 bitti");
		 
		 
		 
		 
		 //Do-While 
		 int j = 1;
		 do {
			 System.out.println(j);
			 j++;
		 }while(j<=10);
		System.out.println("Do-While dongusu bitti");
		 
	}

}
